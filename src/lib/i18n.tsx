export function LanguageProvider({ children }: { children: ReactNode }) {
  // Lazy init reads localStorage on the client during first render.
  // Safe because this provider is in a "use client" boundary and only
  // mounts on the client. SSR falls back to "en".
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window === "undefined") return "en";
    const saved = localStorage.getItem("bitanas-lang") as Lang | null;
    return saved === "ur" ? "ur" : "en";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("bitanas-lang", l);
  };

  const t = (key: string) => STRINGS[lang][key] ?? STRINGS.en[key] ?? key;

  return (
    <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>
  );
}

export function useLang() {
  const ctx = useContext(Ctx);
  if (!ctx) {
    // Safe fallback so components render even outside provider
    return {
      lang: "en" as Lang,
      setLang: () => {},
      t: (key: string) => STRINGS.en[key] ?? key,
    };
  }
  return ctx;
}
